// <================= Objects =================>

let person = {
  name: "Kane",
  age: 21,
}; // Object literal

console.log(person);

// the value of a property can be changed in 2 ways.

// Dot Notation
person.name = "Bryan";
console.log(person.name); // Bryan

// Bracket Notation
person["name"] = "Mary";
console.log(person['name']); // Mary


